
public class Product {
	
	private String pid;
	private String pdescription;
	private int pqty;
	private int prate;
	private int preorderlvl;
	public String getPdescription() {
		return pdescription;
	}
	public void setPdescription(String pdescription) {
		this.pdescription = pdescription;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public int getPqty() {
		return pqty;
	}
	public void setPqty(int pqty) {
		this.pqty = pqty;
	}
	public int getPrate() {
		return prate;
	}
	public void setPrate(int prate) {
		this.prate = prate;
	}
	public int getPreorderlvl() {
		return preorderlvl;
	}
	public void setPreorderlvl(int preorderlvl) {
		this.preorderlvl = preorderlvl;
	}

}
